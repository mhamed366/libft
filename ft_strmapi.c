#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*res;
	size_t			i;
	int				j;

	if (!s || !f)
		return (NULL);
	i = 0;
	j = ft_strlen(s);
	if (!(res = (char*)malloc((j + 1) * sizeof(char))))
		return (NULL);
	while (s[i])
	{
		res[i] = f(i, s[i]);
		i++;
	}
	res[i] = '\0';
	return (res);
}
